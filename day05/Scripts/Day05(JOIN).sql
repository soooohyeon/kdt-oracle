-- 1) 왼쪽 외부조인 (LEFT OUTER JOIN)
-- BOOKS 테이블 기준 왼쪽 외부조인을 수행하고 BOOKSPRICE 테이블의 데이터 포함
-- (왼쪽 테이블의 데이터가 없어도 결과에 포함)
SELECT TB.BOOK_ID, TB.BOOK_TITLE, TB.BOOK_AUTHOR, TP.BP_ID, TP.BP_PRICE
FROM TBL_BOOKS TB
LEFT OUTER JOIN TBL_BOOKPRICE TP
ON TB.BOOK_ID = TP.BP_ID;

-- 2) 오른쪽 외부조인 (RIGHT OUTER JOIN)
-- BOOKS 테이블 기준 오른쪽 외부조인을 수행하고 BOOKSPRICE 테이블의 데이터 포함
-- (오른쪽 테이블의 데이터가 없어도 결과에 포함)
SELECT TB.BOOK_ID, TB.BOOK_TITLE, TB.BOOK_AUTHOR, TP.BP_ID, TP.BP_PRICE
FROM TBL_BOOKS TB
RIGHT OUTER JOIN TBL_BOOKPRICE TP
ON TB.BOOK_ID = TP.BP_ID;

