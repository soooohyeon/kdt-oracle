-- 회원의 이름과 대여한 책의 제목만 가져오는 등가조인
-- > TBL_BOOKS과 TBL_MEMBER가 동일한 컬럼이 없는 경우
SELECT TB.BOOK_TITLE, TM.MEM_NAME
FROM TBL_RENTAL TR	-- 선행테이블
JOIN TBL_MEMBER TM ON TR.MEM_ID = TM.MEM_ID		-- 후행 테이블
JOIN TBL_BOOKS TB ON TR.MEM_ID = TB.BOOK_ID;	-- 후행 테이블

-- 비등가조인 : 두 테이블가느이 조건이 같음이 아닌 범위 조건 등을 사용하는 방식

-- 책 대여일 ~ 반납일 사이에 특정 날짜가 포함되어있는지 조회
SELECT TM.MEM_NAME, TB.BOOK_TITLE, TR.REN_RENTALDATE, TR.REN_RETURNDATE
FROM TBL_RENTAL TR
JOIN TBL_BOOKS TB ON TR.BOOK_ID = TB.BOOK_ID
JOIN TBL_MEMBER TM ON TR.MEM_ID = TM.MEM_ID
WHERE TO_DATE('2023-12-25', 'YYYY-MM-DD')
	BETWEEN TR.REN_RENTALDATE AND TR.REN_RETURNDATE;

-- 대여 기간에 따라 책의 대여 상태 확인
-- 7일 이하 단기 대여, 7일 이상 장기 대여
SELECT TB.BOOK_TITLE, TM.MEM_NAME, TR.REN_RENTALDATE, TR.REN_RETURNDATE,
	CASE
		WHEN TR.REN_RETURNDATE - TR.REN_RENTALDATE <= 7 THEN '단기대여'
		ELSE '장기대여'
	END AS RENTAL_TYPE
FROM TBL_RENTAL TR
JOIN TBL_BOOKS TB ON TR.BOOK_ID = TB.BOOK_ID
JOIN TBL_MEMBER TM ON TR.MEM_ID = TM.MEM_ID;
	
-- 실행순서 :  FROM → ON → JOIN → WHERE → GROUP BY → HAVING → SELECT → ORDER BY
-- FROM : 테이블의 뷰나 데이터 가져오기
-- ON : JOIN절의 조건 평가
-- JOIN : 지정된 조건에 따라 두 개 이상의 컬럼 사용
-- WHERE : 조인 결과에서 조건에 맞는 행 필터링
-- GROUP BY : 데이터 그룹화 (집계함수와 주로 사용)
-- HAVING : GROUP BY로 그룹화된 데이터에 대해 조건 작성
-- SELECT : 조회할 컬럼을 선택(집계함수, 별칭)
-- ORDER BY : 최종 결과 정렬 (ASC, DESC)
-- ** ON절과 WHERE절을 나눠서 작성했을 떄 결과가 같다면 ON절로만 작성하기

